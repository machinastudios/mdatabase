package com.machina.mdatabase.database;

public enum OpType {
    /**
     * AND operation
     */
    AND,

    /**
     * OR operation
     */
    OR,

    /**
     * NOT operation
     */
    NOT,

    /**
     * Equals operation
     */
    EQ,

    /**
     * Not equals operation
     */
    NEQ,

    /**
     * Greater than operation
     */
    GT,

    /**
     * Less than operation
     */
    LT,

    /**
     * Greater than or equal to operation
     */
    GTE,

    /**
     * Less than or equal to operation
     */
    LTE,
}
